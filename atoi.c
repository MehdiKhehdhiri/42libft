int	ft_atoi(const char *nptr)
{
	int	i;
	int	r;
	int	sign;

	sign = 1;
	r = 0;
	i = 0;
	if (nptr[i] == '\0')
		return (0);
	if (nptr[0] == '-')
	{
		sign = -1;
		i++;
	}
	if (nptr[0] == '+')
		i++;
	while (nptr[i] >= '0' && nptr[i] <= '9')
	{
		r = (r * 10) + (nptr[i] - 48);
		i++;
	}
	return (r * sign);
}
