#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	*f;

	if (c > 256)
		c -= 256;
	f = (char *)s;
	while (*f != c)
	{
		if (*f == '\0')
			return (0);
		f++;
	}
	return (f);
}
