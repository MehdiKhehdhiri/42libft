char	*ft_strrchr(const char *s, int c)
{
	char		*f;	
	int			i;

	i = -1;
	f = (char *)s;
	while (*f != '\0')
	{
		i++;
		f++;
	}
	while (*f != c)
	{
		if (i == 0)
			return (0);
		f--;
	}
	return (f);
}
